#include <stdio.h>

int main() {
  printf("Hello World!");
  printf("Hello World2!");
  /*
    Multi-line comments.
  */
  printf("Hello World2! \n");
  return 0;
}

//notes:
//stdio is a header file (essentially a library).
//it helps us work with input and output functions like printf - used to print text to the screen.
//printf does not print a new line.
//header files like stdio allow us to add functionality to C programs.
//main is the main method function used to execute C code.
//; is used to end every C statement.
//return 0 is used to end the execution of the main function.
//use \n to print new line in printf statement.